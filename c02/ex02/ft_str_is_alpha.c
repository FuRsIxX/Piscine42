int	ft_str_is_alpha(char *str)
{
	int	a;

	a = 0;
	if (str[0] == '\0')
		return (1);
	while (str[a])
	{
		if (str[a] < 'A')
			return (0);
		if (str[a] > 'Z' && str[a] < 'a')
			return (0);
		if (str[a] > 'z')
			return (0);
		a++;
	}
	return (1);
}
