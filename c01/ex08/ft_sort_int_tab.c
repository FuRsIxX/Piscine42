void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int c;

	a = 0;
	c = 0;
	while (c < size)
	{
		while (a < size - 1)
		{
			if (tab[a] > tab[a + 1])
			{
				b = tab[a + 1];
				tab[a + 1] = tab[a];
				tab[a] = b;
			}
			a++;
		}
		a = 0;
		c++;
	}
}
