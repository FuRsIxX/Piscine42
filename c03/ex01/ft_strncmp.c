int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned	int	a;

	a = 0;
	while ((s1[a] || s2[a]) && a < n)
	{
		if (s1[a] != s2[a])
			return (s1[a] - s2[a]);
		a++;
	}
	return (0);
}
