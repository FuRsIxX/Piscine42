#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_combn(int n)
{
	int	i;

	i = n;
	if (0 < n < '10')
	{
		write(1, " ", 1);
		write(1, ",", 1);
	}
	i++;
}

int		main()
{
	ft_print_combn(0);
}
